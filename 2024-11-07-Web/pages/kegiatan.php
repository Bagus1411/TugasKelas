    <h2>Kegiatan pada SMPN 6 Sidoarjo</h2>
    <pre>       SMPN 6 Sidoarjo menawarkan berbagai kegiatan untuk mendukung pengembangan akademik, minat, 
    dan karakter siswa. Berikut beberapa kegiatan utama di SMPN 6 Sidoarjo:

    Kegiatan Akademik:

    1- Olimpiade Sains dan Matematika
    2- Bimbingan belajar tambahan
    3- Program literasi sekolah (membaca dan menulis)

    Ekstrakurikuler Seni dan Budaya:

    1- Tari tradisional dan modern
    2- Paduan suara
    3- Seni lukis dan kriya

    Olahraga:

    1- Sepak bola
    2- Bola voli
    3- Bulu tangkis
    4- Pencak silat

    Kepramukaan:

    Kegiatan pramuka wajib untuk membangun disiplin dan kemandirian
    Pengembangan Karakter:

    Program Pendidikan Karakter seperti gotong royong dan kegiatan sosial
    Kegiatan bakti sosial (kunjungan panti asuhan, donor darah)

    Kegiatan Lingkungan (Green School):

    1- Pengelolaan sampah
    2- Penghijauan sekolah
    3- Program bank sampah

    Organisasi Siswa:

    OSIS (Organisasi Siswa Intra Sekolah) yang mengelola berbagai kegiatan siswa
    MPK (Majelis Perwakilan Kelas) untuk melatih kepemimpinan
    Beragam kegiatan ini bertujuan untuk mengembangkan minat dan bakat siswa di berbagai bidang, 
    sambil membangun karakter dan keterampilan sosial mereka.
    </pre>



